// ==========================================
// ROTA PARA GERAR TOKEN TEMPOR√ÅRIO
// ==========================================
app.post('/api/auth/generate-token', (req, res) => {
  const { secret } = req.body;
  
  if (secret === process.env.SECRET_TOKEN) {
    const tempToken = `TEMP-${Date.now()}-${Math.random().toString(36).substr(2, 9)}`;
    tempTokens.set(tempToken, {
      createdAt: Date.now(),
      used: false
    });
    
    console.log('‚úÖ Token tempor√°rio gerado:', tempToken.substring(0, 20) + '...');
    
    res.json({ 
      success: true, 
      token: tempToken,
      expiresIn: 30
    });
  } else {
    console.log('‚ùå Tentativa de gerar token com secret inv√°lido');
    res.status(401).json({ success: false, error: 'N√£o autorizado' });
  }
});

