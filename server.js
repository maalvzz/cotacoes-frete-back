require('dotenv').config();
const express = require('express');
const cors = require('cors');
const session = require('express-session');
const { createClient } = require('@supabase/supabase-js');

const app = express();

// ==========================================
// CONFIGURA√á√ÉO DO SUPABASE
// ==========================================
const supabaseUrl = process.env.SUPABASE_URL;
const supabaseKey = process.env.SUPABASE_KEY;

if (!supabaseUrl || !supabaseKey) {
    console.error('‚ùå ERRO: SUPABASE_URL ou SUPABASE_KEY n√£o configurados no .env');
    process.exit(1);
}

const supabase = createClient(supabaseUrl, supabaseKey);

console.log('‚úÖ Supabase configurado:', supabaseUrl);

// ==========================================
// SISTEMA DE TOKENS TEMPOR√ÅRIOS
// ==========================================
const tempTokens = new Map();

// Limpa tokens expirados a cada minuto
setInterval(() => {
  const now = Date.now();
  for (const [token, data] of tempTokens.entries()) {
    if (now - data.createdAt > 30000) { // 30 segundos
      tempTokens.delete(token);
    }
  }
}, 60000);

// ==========================================
// MIDDLEWARES
// ==========================================
app.use(cors({
    origin: true,
    credentials: true,
    methods: ['GET', 'POST', 'PUT', 'DELETE', 'HEAD', 'OPTIONS'],
    allowedHeaders: ['Content-Type', 'Authorization']
}));
app.use(express.json());

// Sess√£o (mant√©m usu√°rio logado)
app.use(session({
  secret: process.env.SESSION_SECRET || 'seu-secret-super-seguro-aqui',
  resave: false,
  saveUninitialized: false,
  cookie: { 
    maxAge: 8 * 60 * 60 * 1000, // 8 horas
    httpOnly: true,
    secure: false // mude para true se usar HTTPS
  }
}));

